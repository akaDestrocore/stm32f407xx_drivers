#include <stm32f407xx_system.h>


volatile uint32_t millis = 0;


/********************************************************************************************************/
/* @function name 		- SysTick_Init															 		*/
/*																									 	*/
/* @brief				- This function initializes 1ms interrupt using CMSIS core driver				*/
/*																										*/
/* @return				- none																			*/
/*																										*/
/* @Note				- none																			*/
/********************************************************************************************************/
void SysTick_Init(void)
{
    SysTick_Config(SystemCoreClock / 1000);  // Generate interrupt every 1ms
}

/********************************************************************************************************/
/* @function name 		- SysTick_Handler																*/
/*																										*/
/* @brief				- SysTick interruption handler													*/
/*																										*/
/* @return				- none																			*/
/*																										*/
/* @Note				- none																			*/
/********************************************************************************************************/
void SysTick_Handler(void)
{
    millis++;
}

/********************************************************************************************************/
/* @function name 		- GetTick																		*/
/*																										*/
/* @brief				- return millisecond tick value													*/
/*																										*/
/* @return				- millisecond value																*/
/*																										*/
/* @Note				- none																			*/
/********************************************************************************************************/
uint32_t GetTick(void)
{
    return millis;
}

